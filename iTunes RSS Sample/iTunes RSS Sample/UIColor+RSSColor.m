#import "UIColor+RSSColor.h"

@implementation UIColor (RSSColor)

+ (UIColor*)RSSOrangeColor{
    return [UIColor colorWithRed:245/255.0 green:121/255.0 blue:35/255.0 alpha:1.0];
}

+ (UIColor*)RSSBlackColor{
    return [UIColor colorWithRed:51/255.0 green:51/255.0 blue:51/255.0 alpha:1.0];
}

+ (UIColor*)RSSWhiteColor{
    return [UIColor whiteColor];
}

@end
